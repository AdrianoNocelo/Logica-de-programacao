// < - Menor que
// > - Maior que
// >= - Maior igual
// <= - Menor igual
// == - Igual (Maior parte das linguagens só tem esse) Não verifica o tipo - "1" == 1 = true
// != - Diferente (Maior parte das linguagens só tem esse)
// === - Igual 3x (Mais comum no JS) - Verifica o tipo - "1" === 1 = false
// !== - Diferente 3x (Mais comum no JS) - Verifica o tipo

